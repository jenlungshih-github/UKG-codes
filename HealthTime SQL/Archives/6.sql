        '10415360', '10415632', '10415635', '10416781', '10417802',
        '10417845', '10417861', '10418338', '10418506', '10419632',
        '10420796', '10421049', '10422434', '10423636', '10423945',
        '10424641', '10425032', '10425191', '10425466', '10425683',
        '10425853', '10425930', '10426439', '10426458', '10426467',
        '10427101', '10429427', '10471816', '10492499', '10581228',
        '10592313', '10595423', '10599655', '10640519', '10699553',
        '10700654', '10709689', '10715898', '10754402', '10760249',
        '10779426', '10781453', '10821072', '10824741', '10829301'
    )
    )
SELECT
    e.EMPLID,
    e.MANAGER_EMPLID,
    e.NON_UKG_MANAGER_FLAG,
    CASE 
        WHEN m.EMPLID IS NOT NULL THEN 'YES'
        ELSE 'NO'
    END AS MANAGER_EXISTS_IN_UKG
FROM EmployeeCTE e
    LEFT JOIN [dbo].[UKG_EMPLOYEE_DATA] m
    ON e.MANAGER_EMPLID = m.EMPLID