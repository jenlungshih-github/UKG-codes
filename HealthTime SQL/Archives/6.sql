    EmployeeCTE
    AS
    (
        SELECT
            [EMPLID]
    , [MANAGER_EMPLID]
    , [NON_UKG_MANAGER_FLAG]
        FROM [dbo].[UKG_EMPLOYEE_DATA]
        WHERE [EMPLID] IN (
        '10235400', '10373215', '10401320', '10401447', '10402336',
        '10403560', '10404345', '10405207', '10405770', '10406844',
        '10407170', '10407970', '10409211', '10409420', '10411048',
